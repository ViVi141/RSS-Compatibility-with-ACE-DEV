//------------------------------------------------------------------------------------------------
//! RSS ↔ ACE 兼容层 — 可调常量
//------------------------------------------------------------------------------------------------
class SCR_RSS_ACE_Constants
{
	//==============================================================================================
	// 出血 → 代谢成本增加
	//==============================================================================================

	//! 出血代谢乘数上限：出血 100% 时消耗率增加比例
	static const float BLEED_METABOLIC_MULT = 0.40;

	//==============================================================================================
	// 疼痛 → 运动效率降低（仅移动时生效）
	//==============================================================================================

	//! 疼痛代谢乘数上限：剧痛时消耗率增加比例
	static const float PAIN_METABOLIC_MULT = 0.20;

	//==============================================================================================
	// 健康值下降 → 整体运动能力降低
	//==============================================================================================

	//! 健康值代谢乘数上限：濒死时消耗率增加比例
	static const float HEALTH_METABOLIC_MULT = 0.30;

	//==============================================================================================
	// 内源性肾上腺素代偿
	//==============================================================================================

	//! 轻伤（伤势 << 0.3）代偿比例
	static const float COMPENSATION_LIGHT_INJURY = 0.95;
	//! 重伤（伤势 >> 0.8）代偿比例
	static const float COMPENSATION_SEVERE_INJURY = 0.40;
	//! 重伤阈值（伤势 ≥ 此值时达到代偿下限）
	static const float COMPENSATION_SEVERE_THRESHOLD = 0.80;

	//==============================================================================================
	// 药物浓度归一化基准（nM）
	// 各药物在各自 typical 浓度附近产生 50% 效果，用于浓度→0~1 映射
	//==============================================================================================

	//! Epinephrine 典型有效浓度 [nM]
	static const float EPI_TYPICAL_CONC_NM = 500.0;
	//! Morphine 典型有效浓度 [nM]
	static const float MORPHINE_TYPICAL_CONC_NM = 200.0;
	//! Metoprolol 典型有效浓度 [nM]
	static const float METOPROLOL_TYPICAL_CONC_NM = 400.0;
	//! Phenylephrine 典型有效浓度 [nM]
	static const float PHENYLEPHRINE_TYPICAL_CONC_NM = 300.0;
	//! Naloxone 典型有效浓度 [nM]
	static const float NALOXONE_TYPICAL_CONC_NM = 100.0;

	//==============================================================================================
	// 外源性肾上腺素（ACE注射）
	// 浓度正比于 buff 强度，浓度下降时触发代谢反弹
	//==============================================================================================

	//! 肾上腺素最低起效归一化浓度
	static const float EPI_MIN_EFFECTIVE_LEVEL = 0.05;
	//! 肾上腺素 buff：每 tick 回补 = level × 最大回补率
	static const float EPI_BUFF_RECOVERY_MAX = 0.006; // level=1 时每 tick 回补 0.6%
	//! 肾上腺素 penalty：每下降 0.1 level 触发的小幅惩罚
	static const float EPI_PENALTY_PER_DROP = 0.001; // 每 0.1 level 下降扣 0.1%
	//! 肾上腺素下降判定窗口：上一 level 低于当前×此比例时视为下降
	static const float EPI_DECLINE_THRESHOLD = 0.95;

	//==============================================================================================
	// β受体阻滞剂（Metoprolol）→ stamina 消耗增加
	// β阻滞剂降低心率上限 → 运动时心输出量受限 → 同等强度消耗更多 stamina
	//==============================================================================================

	//! Metoprolol 最大代谢乘数加成
	static const float METOPROLOL_METABOLIC_MULT = 0.15; // level=1 时消耗 +15%
	//! Metoprolol 最低起效归一化浓度
	static const float METOPROLOL_MIN_EFFECTIVE_LEVEL = 0.05;

	//==============================================================================================
	// 血管收缩剂（Phenylephrine）→ stamina 消耗增加
	// α1激动剂导致外周血管收缩 → 肌肉血流减少 → 运动效率下降
	//==============================================================================================

	//! Phenylephrine 最大代谢乘数加成
	static const float PHENYLEPHRINE_METABOLIC_MULT = 0.10; // level=1 时消耗 +10%
	//! Phenylephrine 最低起效归一化浓度
	static const float PHENYLEPHRINE_MIN_EFFECTIVE_LEVEL = 0.05;

	//==============================================================================================
	// 纳洛酮（Naloxone）→ 拮抗吗啡的疼痛抑制
	// naloxone 浓度越高，吗啡的 pain suppression 越被抵消
	// 桥接层读取的 pain intensity 已经包含了吗啡抑制，但 naloxone 会使 pain 反弹
	//==============================================================================================

	//! Naloxone 拮抗效力：level=1 时相当于抵消全部吗啡抑制
	static const float NALOXONE_ANTAGONIST_STRENGTH = 0.80;

	//==============================================================================================
	// 低体力保护阈值
	//==============================================================================================

	//! 开始衰减的 stamina 阈值
	static const float FLOOR_START = 0.20;
	//! 完全衰减的 stamina 阈值
	static const float FLOOR_END = 0.15;

	//==============================================================================================
	// 监控循环间隔
	//==============================================================================================

	static const int MONITOR_INTERVAL_MS = 200;
}
